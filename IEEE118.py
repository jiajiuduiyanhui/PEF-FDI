class IEEE118:
    def __init__(self):
        self.n_bus = 118
        self.branches = len(BRANCH_DATA)
        self.baseMVA = 100.0
        
        self.bus_id = (BUS_DATA[:, 0] - 1).astype(int)
        self.bus_type = BUS_DATA[:, 1].astype(int)
        self.Pd = BUS_DATA[:, 2] / self.baseMVA
        self.Qd = BUS_DATA[:, 3] / self.baseMVA
        self.Vm = BUS_DATA[:, 4]
        self.Va = np.deg2rad(BUS_DATA[:, 5])
        self.from_bus = (BRANCH_DATA[:, 0] - 1).astype(int)
        self.to_bus = (BRANCH_DATA[:, 1] - 1).astype(int)
        self.r = BRANCH_DATA[:, 2]
        self.x = BRANCH_DATA[:, 3]
        self.b = BRANCH_DATA[:, 4]
        self.tap = BRANCH_DATA[:, 5]
        
        z = self.r + 1j * self.x
        self.y_s = 1.0 / z              # 串联导纳 g + jb
        self.g_s = self.y_s.real
        self.b_s = self.y_s.imag
        self.b_c = self.b / 2.0
        
        self.slack_bus = np.where(self.bus_type == 3)[0][0]   # =68 (bus 69)
        self.pv_buses  = np.where(self.bus_type == 2)[0]
        self.pq_buses  = np.where(self.bus_type == 1)[0]
        
    def h(self, branch_idx,Vm, Va, g_s=None, b_s=None, b_c=None):
        if g_s is None:
            g_s = self.g_s
        if b_s is None:
            b_s = self.b_s
        if b_c is None:
            b_c = self.b_c
        j = self.from_bus[branch_idx]
        k = self.to_bus[branch_idx]
        Vj = Vm[j]
        Vk = Vm[k]        
        theta_jk = Va[j] - Va[k]            
        g = g_s[branch_idx]
        b = b_s[branch_idx]
        y = b_c[branch_idx]
        Pjk = Vj**2 * g - Vj * Vk * (g * np.cos(theta_jk) + b * np.sin(theta_jk))
        Pkj = Vk**2 * g - Vj * Vk * (g * np.cos(theta_jk) - b * np.sin(theta_jk))
        Qjk = -Vj**2 * (b + y) - Vj * Vk * (g * np.sin(theta_jk) - b * np.cos(theta_jk))
        Qkj = -Vk**2 * (b + y) + Vj * Vk * (g * np.sin(theta_jk) + b * np.cos(theta_jk))
        return np.array([Pjk, Pkj, Qjk, Qkj, Vj, Vk])
    
    def find_branch(self, from_id, to_id):
        """
        根据母线编号找支路下标。

        from_id, to_id 是论文里的母线编号，从 1 开始。
        返回 branch_idx，是 Python 数组下标，从 0 开始。
        """

        f = from_id - 1
        t = to_id - 1

        idx = np.where(
            (self.from_bus == f) & (self.to_bus == t)
        )[0]

        if len(idx) == 0:
            idx = np.where(
                (self.from_bus == t) & (self.to_bus == f)
            )[0]

        if len(idx) == 0:
            raise ValueError(f"Branch ({from_id}, {to_id}) not found.")

        return idx[0]    
    
